                        <p> <h2><strong>L’</strong><a href="https://www.forbes.fr/technologie/amazon-humilie-google-et-apple-dans-la-bataille-sur-lintelligences-artificielle/">intelligence artificielle</a> <strong>standardisée</strong></h2>

                        Maintenant qu’il est possible de développer certaines des intelligences artificielles les plus avancées du marché en quelques heures sur son propre ordinateur avec un cadre en open source, l’IA va devenir omniprésente. Les logiciels seront plus intelligents et plus performants dans le traitement du langage naturel, de la vision informatique, des systèmes de recommandations et ils seront plus faciles à développer en CMS – <strong>Carson Kahn, Volley.com</strong>.
                        </p>

                        <p> <h2><strong>Les chatbots</strong></h2>

                        Nous verrons davantage de chatbots et nous devrons apprendre à interagir avec. Nous pourrons ainsi leur apprendre ce que nous attendons d’eux – <strong>Chalmers Brown, Due</strong>.
                        </p>

                        <p> <h2><strong>La place grandissante de l’éthique dans le secteur de l’</strong><a href="https://www.forbes.fr/management/maitriser-sa-strategie-dinnovation-la-methode-acme/">innovation</a> <strong>technologique</strong></h2>

                        Nous nous heurtons aux dilemmes éthiques qui sont liés directement à ce que nous créons et comment nous le créons : en orientant des publicités en fonction des facteurs démographiques, l’angoisse de l’automatisation des emplois, le revenu universel et même la fréquence du harcèlement sexuel. Le secteur de la tech évolue plus rapidement que les systèmes éthiques qui l’encadrent – Kieran Snyder, Textio.
                        </p>

                        <p> <h2><strong>La réalité augmentée</strong></h2>

                        Snapchat a aidé à la démocratisation de la réalité augmentée grâce à ses filtres amusants. Plus récemment, <a href="https://www.youtube.com/watch?v=gZhQCVSvq5E">Apple</a> a présenté ARKit, permettant de faciliter l’intégration de la réalité augmentée dans les applications pour iPhones et des entreprises comme Amazon l’on déjà adopté. Je pense que 2018 sera sous le signe de la réalité augmentée – <strong>Thomas Griffin, OptinMonster</strong>.
                        </p>

                        <p> <h2><strong>Des fonctionnalités d’équipements informatiques et de logiciels compatibles avec la réalité augmentée et le machine learning</strong></h2>

                        Bon nombre d’entreprises produisent des fonctionnalités d’équipements et des logiciels compatibles avec la réalité augmentée et le machine learning. Les développeurs savent tirer profit de ces puces et API pour créer des applications permettant de voir le monde différemment et de mieux communiquer avec les machines. Je prédis également que les nouvelles technologies seront utilisées pour contrôler la sécurité, aider à regrouper le big data et à améliorer les processus d’authentification – <strong>Chris Kirby, Voices.com</strong>.
                        </p>

                        <p><h2><strong>Une méthode d’analyse améliorée</strong></h2>

                        L’analyse augmentée automatise les résultats des données à l’aide du machine learning et du langage naturel afin d’automatiser la préparation de données pour pouvoir ensuite les partager. Cette présentation des données aide à leur simplification dans le but d’obtenir un résultat clair tout en fournissant un accès à des outils sophistiqués qui confortent les clients et les employés avec le luxe de pouvoir prendre des décisions au jour le jour en toute confiance et objectivité – <strong>Jeremy Williams, Vyudu</strong>.
                        </p>

                        <p> <h2><strong>La pression pour migrer vers le cloud</strong></h2>

                        Davantage d’entreprises migreront vers le cloud car davantage de PDG le demanderont, ce qui aura pour effet d’angoisser les équipes qui devront appliquer cette décision. Elles rencontreront beaucoup de difficultés et se demanderont si elles ont les compétences ou les équipes nécessaires pour le faire. Le cloud change la donne, mais engendre une certaine pression au sein des entreprises qui souhaitent s’y installer – <strong>Todd Delaughter, Automic Software</strong>.
                        </p>

                        <p> <h2><strong>Les piratages informatiques en constante augmentation</strong></h2>

                        Les piratages seront plus nombreux que jamais, qu’ils soient ciblés ou non. Les attaquants viseront certainement des plateformes et des appareils qui sont mal protégés. Cette tendance est grandissante et elle coûtera de plus en plus cher à ses victimes – <strong>Ivailo Nikolov, SiteGround</strong>.
                        </p>

                        <p> <h2><strong>Les investissements dans le domaine de la cybersécurité</strong></h2>

                        En 2017, les cyber attaques constantes ont bouleversé le secteur de la technologie. Il est aujourd’hui primordial d’investir dans le domaine de la cybersécurité. Cela m’intéresse tout particulièrement de voir comment la tech peut aider les secteurs privés et publics à améliorer leur protection. Le cloud propose des niveaux de sécurité plus importants, alors j’espère que les gens lui feront davantage confiance en 2018 –<strong> Marcus Turner, Enola Labs</strong>.
                        </p>

                        <p><h2><strong>La Blockchain</strong></h2>

                        La technologie de la Blockchain doit être surveillée de près en 2018. La suppression des intermédiaires dans beaucoup de domaines affectera énormément les marchés. Ce système sans faille que nous promet la Blockchain devrait révolutionner le secteur de la finance. Nous assistons également à la naissance de tendances visant à améliorer le confort des consommateurs. Si elles sont bien conçues, ces solutions devraient réussir à tirer leur épingle du jeu – <strong>Jim Higgins, Solutionreach.com</strong>.
                        </p>

                        <p><h2><strong>L’<a href="https://www.forbes.fr/technologie/les-6-specialistes-de-lintelligence-artificielle-et-du-machine-learning-dans-le-monde/">intelligence artificielle</a> conversationnelle</strong></h2>

                        L’<a href="https://www.forbes.fr/technologie/intelligence-artificielle-transhumanisme-nick-bostrom-futur-de-lhumanite/">intelligence artificielle</a> conversationnelle sera la seconde tendance la plus disruptive de 2018. Les applications conversationnelles dévoileront tout leur potentiel dans les secteurs des chaînes logistiques, de la vente, de la production et de l’assurance. Associées au succès des applications orientées vers le consommateur telles qu’Alexa, la télécommande Comcast, etc., la voix et les interactions en langage naturel remplaceront lentement les claviers –<strong> Ram Menon, Avaamo</strong>.
                        </p>

                        <p><h2><strong>Les supports visuels (intégrés aux appareils d’assistance vocale)</strong></h2>

                        L’Echo Show d’Amazon montre bien comment le support visuel est un élément clé de l’utilisation d’assistants intelligents, mais il ne s’agit que de la partie émergée de l’iceberg. Grâce à une collaboration intelligente entre les fabricants de cette technologie et les fournisseurs de support, de grands progrès pourraient être faits dans ce domaine. Nous devrions voir une plus large gamme de fonctionnalités et de personnalisations en temps réel de supports visuels pour les assistants vocaux en 2018 – <strong>Zohar Dayan, Wibbitz</strong>.
                        </p>

                        <p><h2><strong> L’impression 3D</strong></h2>

                        Les imprimantes 3D on changé les processus de fabrication dans de nombreux secteurs et elles sont de plus en plus présentes. Il existe maintenant des imprimantes 3D faciles d’utilisation et peu encombrantes pouvant être utilisées par des particuliers. Leur prix diminue, ce qui les rend davantage accessibles. Les imprimantes 3D continueront de révolutionner différents secteurs en 2018 – <strong>Alexandro Pando, Xyrupt</strong>.
                        </p>
